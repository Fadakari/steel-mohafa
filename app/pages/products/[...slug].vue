  if (!pageData.value || !pageData.value.products || pageData.value.products.length === 0) return null
  
  const prices = pageData.value.products.map(p => p.price).filter(p => p > 0)
  if (prices.length === 0) return null

  const minPrice = Math.min(...prices)
  const maxPrice = Math.max(...prices)

  return {
    "@context": "https://schema.org",
    "@type": "ItemList",
    "itemListElement": pageData.value.products.slice(0, 10).map((prod, index) => ({
      "@type": "ListItem",
      "position": index + 1,
      "item": {
        "@type": "Product",
        "name": prod.name,
        "offers": {
          "@type": "Offer",
          "priceCurrency": "IRT", // تومان
          "price": prod.price,
          "availability": "https://schema.org/InStock"
        }
      }
    }))
  }
})

